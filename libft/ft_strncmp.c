#include	<string.h>

int			ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while (*s1 == *s2 && *s1 != 0 && n--)
    {
		++s1;
		++s2;
    }
	if (!n)
		return (0);
	return (*s1 - *s2);
}
