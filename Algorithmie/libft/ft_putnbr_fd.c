#include	<unistd.h>

void		ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n / 10)
		ft_putnbr_fd(n / 10, fd);
	if (n < 0 && n > -10)
		write(fd, "-", 1);
	n %= 10;
	if (n < 0)
		n *= -1;
	c = n + '0';
	write(fd, &c, 1);
}

